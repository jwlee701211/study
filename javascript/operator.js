//"use strict" // 선언된 변수만 사용가능하게 정의!!!

console.log("my" + "cat");
console.log("1" + 2);
console.log(`string literals : 1 + 2 = ${1 + 2}`);
console.log(`0 == false, ${0 == false}`);
console.log(`0 === false, ${0 === false}`);
console.log(`"" == false, ${"" == false}`);
console.log(`"" === false, ${"" === false}`);
console.log(`null == undefined, ${null == undefined}`);
console.log(`null === undefined, ${null === undefined}`);
